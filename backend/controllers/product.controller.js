let slangTableEnsured = false;
let videoTrackColumnEnsured = false;

const TRACK_VALUES = new Set(['chinese', 'english']);

const normalizeLanguageTrack = (rawValue, fallback = 'chinese') => {
    const normalized = String(rawValue || '').trim().toLowerCase();

    if (!normalized) {
        return fallback;
    }

    if (normalized === 'all') {
        return 'all';
    }

    if (['en', 'eng', 'english'].includes(normalized)) {
        return 'english';
    }

    if (['zh', 'cn', 'zho', 'chinese', 'mandarin'].includes(normalized)) {
        return 'chinese';
    }

    if (fallback === 'all') {
        return 'all';
    }

    return TRACK_VALUES.has(fallback) ? fallback : 'chinese';
};

const ensureVideoSlangTable = async () => {
    if (slangTableEnsured) {
        return;
    }

    if (db.clientType === 'postgres') {
        await db.promise().query(`
            CREATE TABLE IF NOT EXISTS video_slang (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                video_id INTEGER NOT NULL,
                word_cn VARCHAR(120) NOT NULL,
                pinyin VARCHAR(120) NULL,
                meaning_vi TEXT NULL,
                tone_note TEXT NULL,
                usage_note TEXT NULL,
                example_cn_1 TEXT NULL,
                example_pinyin_1 TEXT NULL,
                example_vi_1 TEXT NULL,
                example_cn_2 TEXT NULL,
                example_pinyin_2 TEXT NULL,
                example_vi_2 TEXT NULL,
                sort_order INTEGER NOT NULL DEFAULT 0,
                created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
                updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
            )
        `);
        await db.promise().query('CREATE INDEX IF NOT EXISTS idx_video_slang_video ON video_slang(video_id)');
        await db.promise().query('CREATE INDEX IF NOT EXISTS idx_video_slang_sort ON video_slang(video_id, sort_order)');

        slangTableEnsured = true;
        return;
    }

    await db.promise().query(`
        CREATE TABLE IF NOT EXISTS video_slang (
            id INT AUTO_INCREMENT PRIMARY KEY,
            video_id INT NOT NULL,
            word_cn VARCHAR(120) NOT NULL,
            pinyin VARCHAR(120) NULL,
            meaning_vi TEXT NULL,
            tone_note TEXT NULL,
            usage_note TEXT NULL,
            example_cn_1 TEXT NULL,
            example_pinyin_1 TEXT NULL,
            example_vi_1 TEXT NULL,
            example_cn_2 TEXT NULL,
            example_pinyin_2 TEXT NULL,
            example_vi_2 TEXT NULL,
            sort_order INT NOT NULL DEFAULT 0,
            created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
            KEY idx_video_slang_video (video_id),
            KEY idx_video_slang_sort (video_id, sort_order)
        ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4
    `);

    slangTableEnsured = true;
};

const ensureVideoTrackColumn = async () => {
    if (videoTrackColumnEnsured) {
        return;
    }

    if (db.clientType === 'postgres') {
        await db.promise().query(`
            ALTER TABLE videos
            ADD COLUMN IF NOT EXISTS language_track VARCHAR(16) NOT NULL DEFAULT 'chinese'
        `);
        await db.promise().query(`
            UPDATE videos
            SET language_track = 'chinese'
            WHERE language_track IS NULL OR TRIM(language_track) = ''
        `);
        await db.promise().query('CREATE INDEX IF NOT EXISTS idx_videos_language_track ON videos(language_track)');

        videoTrackColumnEnsured = true;
        return;
    }

    const [columnRows] = await db.promise().query("SHOW COLUMNS FROM videos LIKE 'language_track'");
    if (!columnRows.length) {
        await db.promise().query(
            "ALTER TABLE videos ADD COLUMN language_track ENUM('chinese','english') NOT NULL DEFAULT 'chinese' AFTER hsk_level"
        );
    }

    await db.promise().query("UPDATE videos SET language_track = 'chinese' WHERE language_track IS NULL OR language_track = ''");

    const [indexRows] = await db.promise().query("SHOW INDEX FROM videos WHERE Key_name = 'idx_videos_language_track'");
    if (!indexRows.length) {
        await db.promise().query('ALTER TABLE videos ADD INDEX idx_videos_language_track (language_track)');
    }

    videoTrackColumnEnsured = true;
};

const extractR2KeyFromPublicUrl = (fileUrl) => {
    const publicBase = String(process.env.R2_PUBLIC_URL || '').replace(/\/$/, '');
    if (!publicBase || !fileUrl || !String(fileUrl).startsWith(`${publicBase}/`)) {
        return null;
    }

    return String(fileUrl).slice(publicBase.length + 1);
};

const deleteVideoAsset = async (fileUrl) => {
    if (!fileUrl) {
        return;
    }

    const r2Key = extractR2KeyFromPublicUrl(fileUrl);
    if (r2Key) {
        try {
            await deleteR2File(r2Key);
        } catch (error) {
            console.warn('Cannot delete R2 file:', r2Key, error.message || error);
        }
        return;
    }

    if (String(fileUrl).startsWith('/')) {
        const fullPath = path.resolve(__dirname, '..', `.${fileUrl}`);
        if (fs.existsSync(fullPath)) {
            try {
                fs.unlinkSync(fullPath);
            } catch (error) {
                console.warn('Cannot delete local file:', fullPath, error.message || error);
            }
        }
    }
};

const cleanSlangText = (value) => {
    if (value === null || value === undefined) {
        return null;
    }

    const text = String(value).trim();
    return text ? text : null;
};

const normalizeSlangEntries = (entries) => {
    if (!Array.isArray(entries)) {
        return [];
    }

    const normalized = [];
    let sortOrder = 1;

    for (const entry of entries) {
        if (!entry || typeof entry !== 'object') {
            continue;
        }

        const word = cleanSlangText(entry.word_cn ?? entry.word);
        if (!word) {
            continue;
        }

        normalized.push({
            word_cn: word,
            pinyin: cleanSlangText(entry.pinyin),
            meaning_vi: cleanSlangText(entry.meaning_vi ?? entry.meaning),
            tone_note: cleanSlangText(entry.tone_note ?? entry.tone),
            usage_note: cleanSlangText(entry.usage_note ?? entry.usage),
            example_cn_1: cleanSlangText(entry.example_cn_1),
            example_pinyin_1: cleanSlangText(entry.example_pinyin_1),
            example_vi_1: cleanSlangText(entry.example_vi_1),
            example_cn_2: cleanSlangText(entry.example_cn_2),
            example_pinyin_2: cleanSlangText(entry.example_pinyin_2),
            example_vi_2: cleanSlangText(entry.example_vi_2),
            sort_order: Math.max(1, Number(entry.sort_order || sortOrder)),
        });

        sortOrder += 1;
    }

    return normalized;
};

const resolveValidCategoryId = async (rawCategoryId) => {
    const parsed = Number(rawCategoryId);

    if (Number.isFinite(parsed) && parsed > 0) {
        const [exactRows] = await db.promise().query('SELECT id FROM categories WHERE id = ? LIMIT 1', [parsed]);
        if (exactRows.length) {
            return Number(exactRows[0].id);
        }
    }

    const [fallbackRows] = await db.promise().query(
        'SELECT id FROM categories ORDER BY sort_order ASC, id ASC LIMIT 1'
    );

    if (fallbackRows.length) {
        return Number(fallbackRows[0].id);
    }

    // Allow NULL when category master data is not seeded yet.
    return null;
};

// Lấy danh sách Video (Convert từ get_videos.php)
exports.getVideos = async (req, res) => {
    try {
        await ensureVideoTrackColumn();

        const {
            hsk_level,
            category,
            search,
            status,
            page = 1,
            limit = 20,
            sort = 'newest',
            track = 'all',
        } = req.query;
        const safePage = Math.max(1, Number(page) || 1);
        const safeLimit = Math.max(1, Math.min(50, Number(limit) || 20));
        const offset = (safePage - 1) * safeLimit;
        const safeTrack = normalizeLanguageTrack(track, 'all');
        
        let query = 'SELECT v.*, c.name as category_name FROM videos v LEFT JOIN categories c ON v.category_id = c.id WHERE 1=1';
        const params = [];

        if (hsk_level) {
            query += ' AND v.hsk_level = ?';
            params.push(parseInt(hsk_level));
        }

        if (category) {
            query += ' AND v.category_id = ?';
            params.push(parseInt(category));
        }

        if (safeTrack !== 'all') {
            query += ' AND v.language_track = ?';
            params.push(safeTrack);
        }

        if (search) {
            query += ' AND (v.title LIKE ? OR v.title_cn LIKE ?)';
            params.push(`%${search}%`, `%${search}%`);
        }

        if (status === 'published') {
            query += ' AND v.is_published = 1';
        } else if (status === 'draft') {
            query += ' AND v.is_published = 0';
        }

        if (sort === 'popular') {
            query += ' ORDER BY v.view_count DESC, v.created_at DESC';
        } else if (sort === 'az') {
            query += ' ORDER BY v.title ASC';
        } else if (sort === 'duration') {
            query += ' ORDER BY v.duration ASC';
        } else {
            query += ' ORDER BY v.created_at DESC';
        }

        query += ' LIMIT ? OFFSET ?';
        params.push(safeLimit, offset);

        const [rows] = await db.promise().query(query, params);
        
        // Đếm tổng số lượng (Phục vụ phân trang)
        const countQuery = query.split('ORDER BY')[0].replace('SELECT v.*, c.name as category_name', 'SELECT COUNT(*) as total');
        const [countRows] = await db.promise().query(countQuery, params.slice(0, -2));
        const total = countRows[0].total;
        const totalPages = Math.ceil(total / safeLimit);

        res.status(200).json({
            success: true,
            data: rows,
            pagination: {
                total,
                page: safePage,
                limit: safeLimit,
                totalPages,
                total_pages: totalPages,
            }
        });

    } catch (error) {
        console.error("Lỗi lấy danh sách video: ", error);
        res.status(500).json({ success: false, error: 'Loi server khi lay du lieu video.' });
    }
};

// Hàm lấy 1 video (Convert từ get_video.php)
exports.getVideoById = async (req, res) => {
    try {
        await ensureVideoTrackColumn();

        const { id } = req.params;
        const query = `
            SELECT v.*, c.name AS category_name
            FROM videos v
            LEFT JOIN categories c ON v.category_id = c.id
            WHERE v.id = ?
            LIMIT 1
        `;
        const [rows] = await db.promise().query(query, [id]);

        if (rows.length === 0) {
            return res.status(404).json({ success: false, message: 'Khong tim thay video!' });
        }

        // Giữ tương thích logic cũ: mỗi lần mở chi tiết video sẽ tăng view.
        await db.promise().query('UPDATE videos SET view_count = view_count + 1 WHERE id = ?', [id]);
        rows[0].view_count = Number(rows[0].view_count || 0) + 1;

        res.status(200).json({ success: true, data: rows[0] });
    } catch (error) {
        console.error("Lỗi lấy chi tiết video: ", error);
        res.status(500).json({ success: false, message: 'Loi server khi lay chi tiet video.' });
    }
};

// Hàm xóa video (Convert từ delete_video.php)
exports.deleteVideo = async (req, res) => {
    try {
        const { id } = req.params;

        const [rows] = await db.promise().query(
            `
            SELECT id, video_url, thumbnail_url, subtitle_cn_url, subtitle_vi_url, subtitle_pinyin_url
            FROM videos
            WHERE id = ?
            LIMIT 1
            `,
            [id]
        );

        if (!rows.length) {
            return res.status(404).json({ success: false, message: 'Video khong ton tai de xoa!' });
        }

        const video = rows[0];

        await Promise.all([
            deleteVideoAsset(video.video_url),
            deleteVideoAsset(video.thumbnail_url),
            deleteVideoAsset(video.subtitle_cn_url),
            deleteVideoAsset(video.subtitle_vi_url),
            deleteVideoAsset(video.subtitle_pinyin_url),
        ]);

        await ensureVideoSlangTable();

        const conn = await db.promise().getConnection();
        try {
            await conn.beginTransaction();

            await conn.query('DELETE FROM user_progress WHERE video_id = ?', [id]);

            const [vocabRows] = await conn.query('SELECT id FROM video_vocabulary WHERE video_id = ?', [id]);
            if (vocabRows.length) {
                const vocabIds = vocabRows.map((row) => row.id);
                await conn.query('DELETE FROM user_saved_words WHERE vocabulary_id IN (?)', [vocabIds]);
            }

            await conn.query('DELETE FROM video_vocabulary WHERE video_id = ?', [id]);
            await conn.query('DELETE FROM video_slang WHERE video_id = ?', [id]);
            await conn.query('DELETE FROM videos WHERE id = ?', [id]);

            await conn.commit();
        } catch (error) {
            await conn.rollback();
            throw error;
        } finally {
            conn.release();
        }

        res.status(200).json({ success: true, message: 'Xoa video thanh cong!' });
    } catch (error) {
        console.error("Lỗi xóa video: ", error);
        res.status(500).json({ success: false, message: 'Loi server. Khong the xoa video.' });
    }
};

// Helper giả lập slang_helpers.php cũ
const saveSlangEntries = async (videoId, slangEntries) => {
    await ensureVideoSlangTable();
    await db.promise().query('DELETE FROM video_slang WHERE video_id = ?', [videoId]);
    const sanitizedEntries = normalizeSlangEntries(slangEntries);
    if (!sanitizedEntries.length) {
        return;
    }

    const values = sanitizedEntries.map((slang) => [
        videoId,
        slang.word_cn,
        slang.pinyin,
        slang.meaning_vi,
        slang.tone_note,
        slang.usage_note,
        slang.example_cn_1,
        slang.example_pinyin_1,
        slang.example_vi_1,
        slang.example_cn_2,
        slang.example_pinyin_2,
        slang.example_vi_2,
        slang.sort_order,
    ]);

    const query = `
        INSERT INTO video_slang
            (video_id, word_cn, pinyin, meaning_vi, tone_note, usage_note,
                example_cn_1, example_pinyin_1, example_vi_1,
                example_cn_2, example_pinyin_2, example_vi_2, sort_order)
        VALUES ?
    `;
    await db.promise().query(query, [values]);
};

// Thêm mới chức năng Upload Video Metadata (từ upload_video.php)
exports.uploadVideo = async (req, res) => {
    try {
        await ensureVideoTrackColumn();

        const { 
            title, title_cn, description, category_id, hsk_level, is_free, duration,
            video_url, thumbnail_url, subtitle_cn_url, subtitle_vi_url, subtitle_pinyin_url, slang_entries,
            is_published, language_track,
        } = req.body;

        const safeTitle = String(title || '').trim();
        const safeVideoUrl = String(video_url || '').trim();

        if (!safeTitle) {
            return res.status(400).json({ success: false, error: 'Tiêu đề video là bắt buộc' });
        }

        if (!safeVideoUrl) {
            return res.status(400).json({ success: false, error: 'Video URL là bắt buộc' });
        }

        const isUrlLike = /^https?:\/\//i.test(safeVideoUrl) || safeVideoUrl.startsWith('/');
        if (!isUrlLike) {
            return res.status(400).json({ success: false, error: 'Video URL không hợp lệ' });
        }

        const safeCategoryId = await resolveValidCategoryId(category_id);
        const safeLanguageTrack = normalizeLanguageTrack(language_track, 'chinese');

        let safeHskLevel = Number(hsk_level || 1);
        if (Number.isNaN(safeHskLevel) || safeHskLevel < 1 || safeHskLevel > 6) {
            safeHskLevel = 1;
        }

        const safeDuration = Math.max(0, Number(duration || 0));
        const safeTitleCn = String(title_cn || '').trim();
        const safeDescription = String(description || '').trim();
        const safeThumbnailUrl = thumbnail_url ? String(thumbnail_url).trim() : null;
        const safeSubtitleCnUrl = subtitle_cn_url ? String(subtitle_cn_url).trim() : null;
        const safeSubtitleViUrl = subtitle_vi_url ? String(subtitle_vi_url).trim() : null;
        const safeSubtitlePinyinUrl = subtitle_pinyin_url ? String(subtitle_pinyin_url).trim() : null;

        const query = `
            INSERT INTO videos 
            (title, title_cn, description, category_id, hsk_level, language_track, is_free, duration, 
             video_url, thumbnail_url, subtitle_cn_url, subtitle_vi_url, subtitle_pinyin_url, is_published) 
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        `;
        const params = [
            safeTitle, safeTitleCn, safeDescription, safeCategoryId, safeHskLevel, safeLanguageTrack, is_free ? 1 : 0, safeDuration,
            safeVideoUrl, safeThumbnailUrl, safeSubtitleCnUrl, safeSubtitleViUrl, safeSubtitlePinyinUrl,
            is_published === undefined ? 1 : (is_published ? 1 : 0),
        ];

        const [result] = await db.promise().query(query, params);
        if (slang_entries && slang_entries.length > 0) await saveSlangEntries(result.insertId, slang_entries);

        res.status(200).json({ success: true, message: 'Thêm video thành công', data: { id: result.insertId } });
    } catch (error) {
        console.error('Error uploadVideo:', error);
        res.status(500).json({ success: false, error: 'Lỗi server khi upload metadata.' });
    }
};

// Thêm mới chức năng Update Video Metadata (từ update_video.php)
exports.updateVideo = async (req, res) => {
    try {
        await ensureVideoTrackColumn();

        const { id } = req.params;
        const { 
            title, title_cn, description, category_id, hsk_level, is_free,
            duration, is_published,
            video_url, thumbnail_url, subtitle_cn_url, subtitle_vi_url, subtitle_pinyin_url, slang_entries,
            language_track,
        } = req.body;

        const videoId = Number(id);
        if (!videoId) {
            return res.status(400).json({ success: false, error: 'Video ID là bắt buộc' });
        }

        const safeTitle = String(title || '').trim();
        if (!safeTitle) {
            return res.status(400).json({ success: false, error: 'Tiêu đề video là bắt buộc' });
        }

        const safeVideoUrl = String(video_url || '').trim();
        if (!safeVideoUrl) {
            return res.status(400).json({ success: false, error: 'Video URL là bắt buộc' });
        }

        const isUrlLike = /^https?:\/\//i.test(safeVideoUrl) || safeVideoUrl.startsWith('/');
        if (!isUrlLike) {
            return res.status(400).json({ success: false, error: 'Video URL không hợp lệ' });
        }

        const safeCategoryId = await resolveValidCategoryId(category_id);
        const safeLanguageTrack = language_track === undefined
            ? null
            : normalizeLanguageTrack(language_track, 'chinese');

        let safeHskLevel = Number(hsk_level || 1);
        if (Number.isNaN(safeHskLevel) || safeHskLevel < 1 || safeHskLevel > 6) {
            safeHskLevel = 1;
        }

        const hasDuration = duration !== undefined && duration !== null && String(duration).trim() !== '';
        const safeDuration = hasDuration ? Math.max(0, Number(duration || 0)) : null;
        const safeTitleCn = String(title_cn || '').trim();
        const safeDescription = String(description || '').trim();
        const safeThumbnailUrl = thumbnail_url ? String(thumbnail_url).trim() : null;
        const safeSubtitleCnUrl = subtitle_cn_url ? String(subtitle_cn_url).trim() : null;
        const safeSubtitleViUrl = subtitle_vi_url ? String(subtitle_vi_url).trim() : null;
        const safeSubtitlePinyinUrl = subtitle_pinyin_url ? String(subtitle_pinyin_url).trim() : null;

        const [existingRows] = await db.promise().query('SELECT id FROM videos WHERE id = ? LIMIT 1', [videoId]);
        if (!existingRows.length) {
            return res.status(404).json({ success: false, error: 'Không tìm thấy video' });
        }

        const query = `
            UPDATE videos SET 
                title = COALESCE(?, title), title_cn = COALESCE(?, title_cn), 
                description = COALESCE(?, description), category_id = COALESCE(?, category_id), 
                hsk_level = COALESCE(?, hsk_level), language_track = COALESCE(?, language_track), is_free = COALESCE(?, is_free),
                duration = COALESCE(?, duration), is_published = COALESCE(?, is_published),
                video_url = COALESCE(?, video_url), thumbnail_url = COALESCE(?, thumbnail_url),
                subtitle_cn_url = COALESCE(?, subtitle_cn_url), subtitle_vi_url = COALESCE(?, subtitle_vi_url),
                subtitle_pinyin_url = COALESCE(?, subtitle_pinyin_url)
            WHERE id = ?
        `;
        const params = [
            safeTitle, safeTitleCn, safeDescription, safeCategoryId, safeHskLevel, safeLanguageTrack,
            is_free !== undefined ? (is_free ? 1 : 0) : null,
            safeDuration,
            is_published === undefined ? null : (is_published ? 1 : 0),
            safeVideoUrl, safeThumbnailUrl, safeSubtitleCnUrl, safeSubtitleViUrl, safeSubtitlePinyinUrl, videoId
        ];

        await db.promise().query(query, params);
        if (slang_entries) await saveSlangEntries(videoId, slang_entries);

        res.status(200).json({ success: true, message: 'Cập nhật video thành công' });
    } catch (error) {
        console.error('Error updateVideo:', error);
        res.status(500).json({ success: false, error: 'Lỗi server khi cập nhật video.' });
    }
};

