exports.uploadVideo = async (req, res) => {
    try {
        await ensureVideoTrackColumn();

        const { 
            title, title_cn, description, category_id, hsk_level, is_free, duration,
            video_url, thumbnail_url, subtitle_cn_url, subtitle_vi_url, subtitle_pinyin_url, slang_entries,
            is_published, language_track,
        } = req.body;

        const safeTitle = String(title || '').trim();
        const safeVideoUrl = String(video_url || '').trim();

        if (!safeTitle) {
            return res.status(400).json({ success: false, error: 'Tiêu đề video là bắt buộc' });
        }

        if (!safeVideoUrl) {
            return res.status(400).json({ success: false, error: 'Video URL là bắt buộc' });
        }

        const isUrlLike = /^https?:\/\//i.test(safeVideoUrl) || safeVideoUrl.startsWith('/');
        if (!isUrlLike) {
            return res.status(400).json({ success: false, error: 'Video URL không hợp lệ' });
        }

        const safeCategoryId = await resolveValidCategoryId(category_id);
        const safeLanguageTrack = normalizeLanguageTrack(language_track, 'chinese');

        let safeHskLevel = Number(hsk_level || 1);
        if (Number.isNaN(safeHskLevel) || safeHskLevel < 1 || safeHskLevel > 6) {
            safeHskLevel = 1;
        }

        const safeDuration = Math.max(0, Number(duration || 0));
        const safeTitleCn = String(title_cn || '').trim();
        const safeDescription = String(description || '').trim();
        const safeThumbnailUrl = thumbnail_url ? String(thumbnail_url).trim() : null;
        const safeSubtitleCnUrl = subtitle_cn_url ? String(subtitle_cn_url).trim() : null;
        const safeSubtitleViUrl = subtitle_vi_url ? String(subtitle_vi_url).trim() : null;
        const safeSubtitlePinyinUrl = subtitle_pinyin_url ? String(subtitle_pinyin_url).trim() : null;

        const query = `
            INSERT INTO videos 
            (title, title_cn, description, category_id, hsk_level, language_track, is_free, duration, 
             video_url, thumbnail_url, subtitle_cn_url, subtitle_vi_url, subtitle_pinyin_url, is_published) 
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        `;
        const params = [
            safeTitle, safeTitleCn, safeDescription, safeCategoryId, safeHskLevel, safeLanguageTrack, is_free ? 1 : 0, safeDuration,
            safeVideoUrl, safeThumbnailUrl, safeSubtitleCnUrl, safeSubtitleViUrl, safeSubtitlePinyinUrl,
            is_published === undefined ? 1 : (is_published ? 1 : 0),
        ];

        const [result] = await db.promise().query(query, params);
        if (slang_entries && slang_entries.length > 0) await saveSlangEntries(result.insertId, slang_entries);

        res.status(200).json({ success: true, message: 'Thêm video thành công', data: { id: result.insertId } });
    } catch (error) {
        console.error('Error uploadVideo:', error);
        res.status(500).json({ success: false, error: 'Lỗi server khi upload metadata.' });
    }
};

