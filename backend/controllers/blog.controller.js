const pickAvailableGeminiModel = (availableModels = []) => {
    const normalized = Array.from(new Set(
        (Array.isArray(availableModels) ? availableModels : [])
            .map((item) => normalizeGeminiModelName(item))
            .filter(Boolean)
    ));

    if (!normalized.length) {
        return '';
    }

    for (const preferred of GEMINI_MODEL_CANDIDATES) {
        if (normalized.includes(preferred)) {
            return preferred;
        }
    }

    const flashLatest = normalized.find((name) => /flash-latest/i.test(name));
    if (flashLatest) {
        return flashLatest;
    }

    const flashModel = normalized.find((name) => /flash/i.test(name));
    if (flashModel) {
        return flashModel;
    }

    return normalized[0];
};

const resolveGeminiModelName = async (apiKey) => {
    if (cachedResolvedGeminiModel) {
        return cachedResolvedGeminiModel;
    }

    try {
        const response = await axios.get('https://generativelanguage.googleapis.com/v1beta/models', {
            params: { key: apiKey },
            timeout: 15000,
        });

        const availableModels = (response?.data?.models || [])
            .filter((item) => Array.isArray(item?.supportedGenerationMethods)
                && item.supportedGenerationMethods.includes('generateContent'))
            .map((item) => normalizeGeminiModelName(item?.name));

        const selected = pickAvailableGeminiModel(availableModels);
        if (selected) {
            cachedResolvedGeminiModel = selected;
            return selected;
        }
    } catch (error) {
        const message = error?.response?.data?.error?.message || error?.message || String(error);
        console.warn('Warning resolve Gemini model failed:', message);
    }

    cachedResolvedGeminiModel = GEMINI_MODEL_CANDIDATES[0] || 'gemini-flash-latest';
    return cachedResolvedGeminiModel;
};

const parseGeminiJsonArray = (rawText) => {
    const text = String(rawText || '').trim();
    if (!text) {
        return [];
    }

    const candidateTexts = [text];
    const fenced = text.match(/```(?:json)?\s*([\s\S]*?)\s*```/i);
    if (fenced?.[1]) {
        candidateTexts.push(fenced[1].trim());
    }

    const arrayStart = text.indexOf('[');
    const arrayEnd = text.lastIndexOf(']');
    if (arrayStart >= 0 && arrayEnd > arrayStart) {
        candidateTexts.push(text.slice(arrayStart, arrayEnd + 1));
    }

    for (const candidate of candidateTexts) {
        try {
            const cleanedCandidate = String(candidate || '').replace(/,\s*([}\]])/g, '$1');
            const parsed = JSON.parse(cleanedCandidate);
            if (Array.isArray(parsed)) {
                return parsed;
            }

            if (parsed && typeof parsed === 'object') {
                const wrapped = parsed.data
                    || parsed.vocabulary
                    || parsed.words
                    || parsed.items
                    || parsed.result;

                if (Array.isArray(wrapped)) {
                    return wrapped;
                }

                if (parsed.word && (parsed.meaning || parsed.meaning_vi || parsed.gloss)) {
                    return [parsed];
                }
            }
        } catch {
            // Try next candidate.
        }
    }

    throw new Error('Gemini tra ve JSON khong hop le');
};

const buildSubtitlePairs = (cues, meaningCues) => cues
    .map((cue, index) => {
        const cn = normalizeSubtitleLine(cue?.text || '');
        if (!cn || !hasChinese(cn)) {
            return null;
        }

        return {
            cueIndex: index,
            start: Number(cue?.start || 0),
            cn,
            vi: resolveCueMeaning(cue, index, meaningCues),
        };
    })
    .filter(Boolean);

const findSubtitleContextForWord = (word, subtitlePairs) => {
    if (!word || !Array.isArray(subtitlePairs) || !subtitlePairs.length) {
        return null;
    }

    let fallback = null;
    for (const pair of subtitlePairs) {
        if (!fallback) {
            fallback = pair;
        }

        if (pair.cn.includes(word)) {
            return pair;
        }
    }

    return fallback;
};

const normalizeGeminiVocabularyItems = (items, subtitlePairs, limit) => {
    const maxItems = Number.isFinite(limit)
        ? Math.max(10, Math.min(80, Math.floor(limit)))
        : 35;
    const uniqueByWord = new Map();

    for (const item of Array.isArray(items) ? items : []) {
        const word = sanitizeChineseWord(item?.word || item?.term || item?.word_cn);
        if (!word || !hasChinese(word) || word.length < 2 || word.length > 10) {
            continue;
        }

        if (!/^[\u3400-\u9fff]+$/.test(word)) {
            continue;
        }

        if (uniqueByWord.has(word)) {
            continue;
        }

        const context = findSubtitleContextForWord(word, subtitlePairs);
        const meaning = normalizeMeaningText(item?.meaning || item?.meaning_vi || context?.vi || '');

        uniqueByWord.set(word, {
            word,
            pinyin: normalizeSubtitleLine(item?.pinyin || '').slice(0, 120),
            meaning_vi: meaning,
            meaning_source: 'gemini',
            word_type: inferWordType(word),
            timestamp_start: Number((context?.start || 0).toFixed(3)),
            example_sentence: String(context?.cn || '').slice(0, 500),
        });

        if (uniqueByWord.size >= maxItems) {
            break;
        }
    }

    return Array.from(uniqueByWord.values())
        .sort((a, b) => a.timestamp_start - b.timestamp_start)
        .slice(0, maxItems)
        .map((item, index) => ({
            ...item,
            sort_order: index + 1,
        }));
};

