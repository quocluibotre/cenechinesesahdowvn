exports.getWordPronunciation = async (req, res) => {
    try {
        const rawWord = req.query.word || req.query.text || '';
        const word = normalizeSubtitleLine(rawWord).slice(0, 60);

        if (!word) {
            return res.status(400).json({ success: false, error: 'word la bat buoc' });
        }

        const cacheKey = normalizeTtsCacheKey(word);
        const cachedAudio = getCachedTtsAudio(cacheKey);
        if (cachedAudio) {
            res.setHeader('Content-Type', cachedAudio.contentType.includes('audio') ? cachedAudio.contentType : 'audio/mpeg');
            res.setHeader('Cache-Control', 'public, max-age=86400');
            res.setHeader('Content-Length', String(cachedAudio.audioBuffer.length));
            res.setHeader('X-TTS-Cache', 'HIT');
            return res.status(200).send(cachedAudio.audioBuffer);
        }

        let lastError = '';
        for (const buildProvider of TTS_PROVIDER_FACTORIES) {
            const provider = buildProvider(word);

            if (!provider.url) {
                continue;
            }

            try {
                const { audioBuffer, contentType } = await fetchTtsAudioFromProvider(provider.url);
                setCachedTtsAudio(cacheKey, { audioBuffer, contentType });

                res.setHeader('Content-Type', contentType.includes('audio') ? contentType : 'audio/mpeg');
                res.setHeader('Cache-Control', 'public, max-age=86400');
                res.setHeader('Content-Length', String(audioBuffer.length));
                res.setHeader('X-TTS-Cache', 'MISS');

                return res.status(200).send(audioBuffer);
            } catch (error) {
                lastError = error?.message || String(error);
                console.warn(`TTS provider ${provider.name} failed:`, lastError);
            }
        }

        return res.status(502).json({
            success: false,
            error: 'Khong lay duoc audio phat am tu nguon TTS',
            detail: lastError || 'Khong co nguon TTS hoat dong',
        });
    } catch (error) {
        console.error('Error getWordPronunciation:', error);
        return res.status(500).json({ success: false, error: 'Loi server khi tao audio phat am' });
    }
};

exports.generateVocabulary = async (req, res) => {
    let conn;

    try {
        if (req.userRole !== 'admin') {
            return res.status(403).json({ success: false, error: 'Chi admin moi duoc tao tu vung bang AI' });
        }

        const { video_id, videoId, limit: requestedLimit, max_words } = req.body || {};
        const resolvedVideoId = Number(video_id ?? videoId);

        if (!Number.isFinite(resolvedVideoId) || resolvedVideoId <= 0) {
            return res.status(400).json({ success: false, error: 'video_id la bat buoc' });
        }

        const rawLimit = Number(requestedLimit ?? max_words ?? 35);
        const limit = Number.isFinite(rawLimit)
            ? Math.min(80, Math.max(10, Math.floor(rawLimit)))
            : 35;

        const [videos] = await db.promise().query(
            'SELECT id, subtitle_cn_url, subtitle_vi_url, hsk_level FROM videos WHERE id = ? LIMIT 1',
            [resolvedVideoId]
        );

        if (!videos.length) {
            return res.status(404).json({ success: false, error: 'Video khong ton tai' });
        }

        const video = videos[0];
        if (!video.subtitle_cn_url) {
            return res.status(400).json({ success: false, error: 'Video chua co phu de tieng Trung' });
        }

        if (!String(process.env.GEMINI_API_KEY || '').trim()) {
            return res.status(500).json({ success: false, error: 'Thieu GEMINI_API_KEY tren server' });
        }

        const subtitleCnContent = await loadSubtitleContent(video.subtitle_cn_url);

        let subtitleViContent = '';
        if (video.subtitle_vi_url) {
            try {
                subtitleViContent = await loadSubtitleContent(video.subtitle_vi_url);
            } catch (error) {
                console.warn('Warning load subtitle_vi for meaning fallback:', error.message || error);
            }
        }

        const cues = parseSubtitleCues(subtitleCnContent, { requireChinese: true });
        if (!cues.length) {
            return res.status(200).json({ success: false, error: 'Khong doc duoc phu de tieng Trung' });
        }

        const meaningCues = subtitleViContent
            ? parseSubtitleCues(subtitleViContent, { requireChinese: false })
            : [];
        const subtitlePairs = buildSubtitlePairs(cues, meaningCues);

        if (!subtitlePairs.length) {
            return res.status(200).json({ success: false, error: 'Khong du du lieu phu de de tao tu vung' });
        }

        const entries = await generateVocabularyWithGemini(subtitlePairs, limit);

        if (!entries.length) {
            return res.status(200).json({ success: false, error: 'Gemini khong tra ve tu vung hop le' });
        }

        const safeVideoId = resolvedVideoId;
        const safeHskLevel = Number(video.hsk_level) || null;

        conn = await db.promise().getConnection();
        await conn.beginTransaction();

        await conn.query('DELETE FROM video_vocabulary WHERE video_id = ?', [safeVideoId]);

        const insertRows = entries.map((item) => ([
            safeVideoId,
            item.word,
            item.pinyin || '',
            item.meaning_vi || '',
            item.word_type,
            safeHskLevel,
            item.example_sentence,
            item.timestamp_start,
            item.sort_order,
        ]));

        const insertSql = `
            INSERT INTO video_vocabulary
                (video_id, word_cn, pinyin, meaning_vi, word_type, hsk_level, example_sentence, timestamp_start, sort_order)
            VALUES ?
        `;

        await conn.query(insertSql, [insertRows]);
        await conn.commit();
        conn.release();
        conn = null;

        res.status(200).json({
            success: true,
            message: `Da tao ${entries.length} the tu vung bang Gemini`,
            count: entries.length,
            data: entries,
        });
    } catch (error) {
        if (conn) {
            try {
                await conn.rollback();
                conn.release();
            } catch (rollbackError) {
                console.error('Rollback generateVocabulary failed:', rollbackError);
            }
        }

        console.error('Error generateVocabulary:', error);
        res.status(500).json({ success: false, error: 'Loi server khi tao tu vung' });
    }
};
