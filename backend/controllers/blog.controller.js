const generateVocabularyWithGemini = async (subtitlePairs, limit) => {
    const geminiClient = createGeminiClient();
    if (!geminiClient) {
        throw new Error('Chua cau hinh GEMINI_API_KEY');
    }

    const { genAI, apiKey } = geminiClient;
    const resolvedModelName = await resolveGeminiModelName(apiKey);

    const chunkSize = Number.isFinite(GEMINI_CHUNK_SIZE)
        ? Math.max(3, Math.min(20, Math.floor(GEMINI_CHUNK_SIZE)))
        : 10;
    const requestDelayMs = Number.isFinite(GEMINI_REQUEST_DELAY_MS)
        ? Math.max(1200, Math.floor(GEMINI_REQUEST_DELAY_MS))
        : 4200;
    const maxRetries = Number.isFinite(GEMINI_MAX_RETRIES)
        ? Math.max(0, Math.min(4, Math.floor(GEMINI_MAX_RETRIES)))
        : 2;

    let rawItems = [];
    let chunkFailureCount = 0;
    let lastFailureMessage = '';

    for (let i = 0; i < subtitlePairs.length; i += chunkSize) {
        const chunk = subtitlePairs.slice(i, i + chunkSize);
        let lastError = null;

        for (let attempt = 0; attempt <= maxRetries; attempt += 1) {
            try {
                const extracted = await extractVocabularyFromLLM(genAI, resolvedModelName, chunk);
                rawItems = rawItems.concat(extracted || []);
                lastError = null;
                break;
            } catch (error) {
                lastError = error;
                if (attempt < maxRetries) {
                    await sleep(1200 * (attempt + 1));
                }
            }
        }

        if (lastError) {
            chunkFailureCount += 1;
            lastFailureMessage = lastError?.message || String(lastError);
            console.warn(`Gemini model ${resolvedModelName} chunk ${i}-${i + chunk.length - 1} that bai:`, lastFailureMessage);
        }

        const hasNextChunk = i + chunkSize < subtitlePairs.length;
        if (hasNextChunk) {
            await sleep(requestDelayMs);
        }
    }

    const normalized = normalizeGeminiVocabularyItems(rawItems, subtitlePairs, limit);
    if (!normalized.length && chunkFailureCount > 0) {
        throw new Error(lastFailureMessage || 'Gemini khong tao duoc ket qua hop le');
    }

    return normalized;
};

